#[test]
fn test_ci_validate_bitbucket_fails_when_pipeline_is_stale() {
    let dir = TempDir::new().expect("temp dir");
    setup_project(&dir, "fresh-app");
    fs::write(
        dir.path().join("bitbucket-pipelines.yml"),
        "# Bitbucket Pipelines generated by Stacker for stale-app\npipelines:\n  branches:\n    main:\n      - step:\n          name: Deploy stale-app\n",
    )
    .expect("write stale pipeline");

    stacker_cmd()
        .current_dir(dir.path())
        .args(["ci", "validate", "--platform", "bitbucket"])
        .assert()
        .failure()
        .stderr(predicate::str::contains("out of sync"));
}


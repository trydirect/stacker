use assert_cmd::Command;
use predicates::prelude::*;
use std::fs;
use tempfile::TempDir;

fn stacker_cmd() -> Command {
    Command::cargo_bin("stacker-cli").expect("stacker-cli binary not found")
}

fn setup_project(dir: &TempDir, name: &str) {
    let config = format!(
        r#"
name: {name}
version: "1.0"
app:
  type: static
  path: "."
deploy:
  target: local
"#
    );

    fs::write(dir.path().join("stacker.yml"), config).expect("write stacker.yml");
    fs::write(dir.path().join("index.html"), "<h1>Hello</h1>").expect("write index.html");
}

#[test]
fn test_ci_export_bitbucket_generates_pipeline_file() {
    let dir = TempDir::new().expect("temp dir");
    setup_project(&dir, "bitbucket-app");

    stacker_cmd()
        .current_dir(dir.path())
        .args(["ci", "export", "--platform", "bitbucket"])
        .assert()
        .success()
        .stdout(predicate::str::contains("bitbucket-pipelines.yml"));

    let pipeline = fs::read_to_string(dir.path().join("bitbucket-pipelines.yml"))
        .expect("bitbucket pipeline should be written");
    assert!(pipeline.contains("Bitbucket Pipelines generated by Stacker for bitbucket-app"));
    assert!(pipeline.contains("stacker deploy --target local"));
    assert!(pipeline.contains("STACKER_TOKEN"));
}


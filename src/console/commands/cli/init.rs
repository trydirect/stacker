/// `stacker init [--type static|node|python|rust|go|php] [--with-proxy] [--with-ai] [--from-github URL]`
///
/// Detects the project type in the current directory and generates
/// a `stacker.yml` configuration file with sensible defaults.
///
/// When `--with-ai` is used with an AI provider configured (via flags
/// or environment variables), Stacker scans the project deeply and
/// sends the context to the AI to generate a tailored `stacker.yml`
/// with appropriate services, proxy, monitoring, etc.
///
/// When `--from-github` is used, Stacker clones the specified GitHub
/// repository into a temp directory, detects the project type and
/// services, and generates a `stacker.yml` in the current directory.
pub struct InitCommand {
    pub app_type: Option<String>,
    pub with_proxy: bool,
    pub with_ai: bool,
    pub with_cloud: bool,
    /// GitHub repository URL (owner/repo or full URL) to generate config from
    pub from_github: Option<String>,
    /// Overwrite existing stacker.yml if present
    pub force: bool,
    /// Override AI provider (openai, anthropic, ollama, custom)
    pub ai_provider: Option<String>,
    /// Override AI model name
    pub ai_model: Option<String>,
    /// Override AI API key
    pub ai_api_key: Option<String>,
    /// Override AI endpoint URL (e.g. http://localhost:11434 for Ollama)
    pub ai_endpoint: Option<String>,
}

impl InitCommand {
    pub fn new(
        app_type: Option<String>,
        with_proxy: bool,
        with_ai: bool,
        with_cloud: bool,
    ) -> Self {
        Self {
            app_type,
            with_proxy,
            with_ai,
            with_cloud,
            from_github: None,
            force: false,
            ai_provider: None,
            ai_model: None,
            ai_api_key: None,
            ai_endpoint: None,
        }
    }

    pub fn with_from_github(mut self, from_github: Option<String>) -> Self {
        self.from_github = from_github;
        self
    }

    pub fn with_force(mut self, force: bool) -> Self {
        self.force = force;
        self
    }

    pub fn with_ai_options(
        mut self,
        provider: Option<String>,
        model: Option<String>,
        api_key: Option<String>,
        endpoint: Option<String>,
    ) -> Self {
        self.ai_provider = provider;
        self.ai_model = model;
        self.ai_api_key = api_key;
        self.ai_endpoint = endpoint;
        self
    }
}

/// Parse an app type string (e.g. "node", "static") into `AppType`.
fn parse_app_type(s: &str) -> Result<AppType, CliError> {
    let json = format!("\"{}\"", s.to_lowercase());
    serde_json::from_str::<AppType>(&json).map_err(|_| {
        CliError::ConfigValidation(format!(
            "Unknown app type '{}'. Valid types: static, node, python, rust, go, php, custom",
            s
        ))
    })
}

fn prompt_line(prompt: &str) -> Result<String, CliError> {
    print!("{}", prompt);
    io::stdout().flush()?;
    let mut input = String::new();
    io::stdin().read_line(&mut input)?;
    Ok(input.trim().to_string())
}

fn prompt_with_default(prompt: &str, default: &str) -> Result<String, CliError> {
    let line = prompt_line(&format!("{} [{}]: ", prompt, default))?;
    if line.is_empty() {
        Ok(default.to_string())
    } else {
        Ok(line)
    }
}

fn prompt_optional(prompt: &str, default: Option<&str>) -> Result<Option<String>, CliError> {
    let formatted = match default {
        Some(value) if !value.trim().is_empty() => format!("{} [{}]: ", prompt, value),
        _ => format!("{}: ", prompt),
    };
    let line = prompt_line(&formatted)?;
    if line.is_empty() {
        Ok(default
            .map(str::trim)
            .filter(|value| !value.is_empty())
            .map(ToOwned::to_owned))
    } else {
        Ok(Some(line))
    }
}

fn prompt_yes_no(prompt: &str, default_yes: bool) -> Result<bool, CliError> {
    let suffix = if default_yes { "[Y/n]" } else { "[y/N]" };
    let line = prompt_line(&format!("{} {}: ", prompt, suffix))?;
    if line.is_empty() {
        return Ok(default_yes);
    }

    match line.to_ascii_lowercase().as_str() {
        "y" | "yes" => Ok(true),
        "n" | "no" => Ok(false),
        _ => Ok(default_yes),
    }
}

fn default_public_domain(config: &StackerConfig) -> String {
    format!(
        "{}.example.com",
        config.name.replace(' ', "-").to_ascii_lowercase()
    )
}

fn default_image_repository(state: &ScenarioState) -> String {
    state
        .vars
        .get("image_repository")
        .cloned()
        .unwrap_or_else(|| {
            let project_name = state
                .vars
                .get("project_name")
                .cloned()
                .unwrap_or_else(|| "website".to_string())
                .replace(' ', "-")
                .to_ascii_lowercase();
            format!("ghcr.io/your-org/{project_name}")
        })
}

